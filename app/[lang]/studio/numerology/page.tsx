function calcBalance(name: string): number {
  const words = name.trim().split(/\s+/);
  const sum = words.reduce((s, w) => {
    const first = w[0]?.toLowerCase() ?? "";
    return s + (LETTER_VALUES[first] ?? 0);
  }, 0);
  return reduceNum(sum || 1);
}

